package com.example.financeiro.api.repository.filter;

public class UsuarioFilter {

	private String nome;

	private String usuario_default;
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getUsuario_default() {
		return usuario_default;
	}

	public void setUsuario_default(String usuario_default) {
		this.usuario_default = usuario_default;
	}
	

}
